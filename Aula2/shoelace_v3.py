A = [1,1]
B = [5,1]
C = [3,3]

pontos = [A, B, C, A]

Area = 0

for i in range(len(pontos)-1):
    Area += pontos[i][0]*pontos[i+1][1] - pontos[i][1]*pontos[i+1][0]

Area = Area / 2

if Area < 0:
    Area = Area * -1

print("Area: " + str(Area))